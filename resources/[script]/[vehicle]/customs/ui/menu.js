function hexToRgb(str) {
	if ( /^#([0-9a-f]{3}|[0-9a-f]{6})$/ig.test(str)  ) {
		let hex = str.substr(1);
		hex = hex.length == 3 ? hex.replace(/(.)/g, '$1$1') : hex;
		let rgb = parseInt(hex, 16);
		return  {
			red: (rgb >> 16) & 255,
			green: (rgb >> 8) & 255,
			blue: rgb & 255
		};
	}
	return false;
}

